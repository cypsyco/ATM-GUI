
public class User {

	//variable for User
	private String name;
	
	//constructor for User
	public User(String name) {
			this.name = name;
		}
	
	//getter and setter methods for User
	public String get_name() {
		return this.name;
	}
	
	public void set_name(String name) {
		this.name = name;
	}
	
}
